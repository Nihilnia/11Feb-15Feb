# bin() Function

"Show me a number as binary"

print(bin(8))

numberX = bin(1231)
print(numberX)


# hex() Function

"Show me a number as hex"

print(hex(88))

numberY = hex(653)
print(numberY)


# abs() Function

"Absolute value"

print(abs(-12))

numberZ = abs(12 - 1920)
print(numberZ)


# round() Function

"Round a number"

print(round(7.8))
print(round(7.4))


# max() Function

"give me the biggest number"

print(max(1, 3, 4, 2, 8, 0))


# min() Function

"give me the smaller number"

print(min(1, 2, 7, -2, -98, -7))


# sum() Function

"sum up! (parameters should be list or tuple, bc int objects is not iterable)"

sumX = sum(range(11))
print("Sum:", sumX)

sumY = sum((1, 2 ,3))
print("SumY:", sumY)

sumZ = sum([4, 5, 6])
print("SumZ:", sumZ)


# pow() Function

powX = pow(2, 3)
print("Pow:", powX)